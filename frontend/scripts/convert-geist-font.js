#!/usr/bin/env node
/**
 * Converts Geist TTF fonts to base64 and generates geist-pdf.ts for jsPDF.
 * Run: node scripts/convert-geist-font.js
 */
const fs = require("fs");
const path = require("path");

const geistPath = path.join(
  __dirname,
  "../node_modules/geist/dist/fonts/geist-sans"
);
const outputPath = path.join(__dirname, "../utils/fonts/geist-pdf.ts");

const regularTtf = fs.readFileSync(
  path.join(geistPath, "Geist-Regular.ttf"),
  { encoding: null }
);
const boldTtf = fs.readFileSync(path.join(geistPath, "Geist-Bold.ttf"), {
  encoding: null,
});

const regularBase64 = Buffer.from(regularTtf).toString("base64");
const boldBase64 = Buffer.from(boldTtf).toString("base64");

const content = `/**
 * Geist font registration for jsPDF.
 * Auto-generated by scripts/convert-geist-font.js
 * Source: geist package (Geist-Regular.ttf, Geist-Bold.ttf)
 */

import type { jsPDF } from "jspdf";

const GEIST_REGULAR_BASE64 = "${regularBase64}";
const GEIST_BOLD_BASE64 = "${boldBase64}";

export function registerGeistFont(doc: jsPDF): void {
  doc.addFileToVFS("Geist-Regular.ttf", GEIST_REGULAR_BASE64);
  doc.addFont("Geist-Regular.ttf", "GeistSans", "normal");

  doc.addFileToVFS("Geist-Bold.ttf", GEIST_BOLD_BASE64);
  doc.addFont("Geist-Bold.ttf", "GeistSans", "bold");
}
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, content, "utf8");
console.log(`Generated ${outputPath}`);
